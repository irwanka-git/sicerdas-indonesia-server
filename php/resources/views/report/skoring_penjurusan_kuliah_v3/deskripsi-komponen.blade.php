				<p>
					<strong>BAKAT KOGNITIF</strong>
				</p>
				<ul>
					<li >
						<em>Informasi Umum</em> = pengetahuan terhadap informasi umum dan hangat yang sedang beredar. Informasi berguna untuk memahami isu-isu mutakhir. Pengetahuan berguna untuk jurusan yang membutuhkan analisis informasi umum, seperti: <strong>Hubungan Internasional, Ilmu Politik, Ilmu Komunikasi, Ilmu Ekonomi, Teknik Industri, dll.</strong>
					</li>
					<li >
						<em>Penalaran Verbal</em> = Penalaran terhadap stimulus kata, bahasa, kalimat, analogi, pengertian. Bakat ini berguna untuk menalar informasi/pekerjaan dengan komunikasi verbal/pembuatan makalah. Jurusan yang membutuhkan kemampuan ini seperti: <strong>Psikologi, Ilmu Hukum, Manajemen, Ilmu Administrasi Negara, Sastra, Sejarah</strong>
					</li>
					<li >
						<em>Penalaran Kuantitatif</em> = penalaran terhadap stimulus angka, bilangan, himpunan, sudut, hitungan sederhana. Bakat ini berguna dengan pekerjaan hitungan atau jurusan kuliah yang banyak membutuhkan hitungan. Jurusan yang membutuhkan, seperti: <strong>Akuntansi, Fiskal (Perpajakan), Matematika, Teknik</strong>pada umumnya
					</li>
					<li >
						<em>Penalaran Abstrak</em> = penalaran terhadap stimulus gambar, simbol, tanda, dll. Bakat ini berguna untuk menalar hal bersifat abstrak seperti proses suatu zat, perkembangan suatu benda, arah suatu benda, dll. Jurusan yang membutuhkan ini seperti <strong>Kedokteran, Kehutanan, Pertanian, Peternakan, Biologi, Kimia, Arsitekstur, Desain.</strong>
					</li>
					<li >
						<em>Pengertian Mekanika</em> = penalaran stimulus fungsi mekanik/fisika sederhana di sekitar kita. Bakat ini berguna untuk mendesain suatu mesin/alat yang berhubungan bagi kehidupan ataupun kuliah <strong>Teknik Mesin/Elektro/Fisika</strong>.
					</li>
					<li >
						<em>Cepat Teliti</em> = kemampuan memperhatikan detail dalam melakukan suatu pekerjaan rutin. Bakat berguna untuk pekerjaan yang terkait dengan keteliitian, misalnya: eskperimen di laboratorium. Ada banyak jurusan yang membutuhkan kegiatan ketelitian.
					</li>
				</ul>
				
